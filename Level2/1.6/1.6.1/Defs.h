// define PRINT_1 Macro
#ifndef PRINT_1
#define PRINT_1(a) printf("The value of a is: %lf\n", a);
#endif

// define PRINT_2 Macro
#ifndef PRINT_2
#define PRINT_2(a, b) printf("The value of a is: %lf\nThe value of b is: %lf\n", a, b);
#endif